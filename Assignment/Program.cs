/* 
A variable is a named storage location in memory used to hold data.

Think of it like a labeled box:
The label is the variable name.
The content inside is the value.

In C#, every variable must have a data type, which tells the computer what kind of data it will store.

Examples of data types:

int → whole numbers (e.g., 5, 20)

string → text (e.g., "Chika")

double → decimal numbers (e.g., 3.14)

bool → true or false*/