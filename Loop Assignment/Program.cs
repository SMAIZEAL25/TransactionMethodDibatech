using System;

class Program
{
    static void Main()
    {
        for (int i = 1; i <= 10; i++)
        {
            Console.WriteLine(i);
        }
    }
}
/* What is a loop?

A loop is used to repeat a block of code multiple times.
We use a loop to automate repetition.
int i = 1 → start at 1

i <= 10 → keep running while i is less than or equal to 10

i++ → increase i by 1 each time
*/
